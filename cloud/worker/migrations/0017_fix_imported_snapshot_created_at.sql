-- 0017_fix_imported_snapshot_created_at.sql — SPEC-031
--
-- Баг: при импорте из Finances.xlsx (25.05.2026) снапшоты записались РАНЬШЕ
-- (created_at = 2026-05-25T17:23:42Z), чем импортированные доходы той же даты
-- (зарплата legacy, created_at = 2026-05-25T19:09:32Z). Снапшот на 15-е уже
-- включает полученную в этот день зарплату (это замер баланса), но из-за tie-break
-- SPEC-024 (ledger.ts:55-58: событие той же даты с created_at > baseline.created_at
-- считается «после снапшота») доход прибавлялся ПОВЕРХ снапшота → двойной учёт
-- (+2330 €/мес) в историческом ряду net worth дашборда («Со всеми фондами»).
--
-- Фикс (данные, не логика — см. SPEC-031 NG1): поднять created_at импортированных
-- снапшотов в конец дня импорта, чтобы импортированные события той же даты
-- сортировались ДО снапшота → снапшот (ground truth дня) поглощает свои события.
-- Значение 23:59:59Z — после всех импортированных T-format событий (макс. 19:09:32Z),
-- в том же T/Z-формате (корректное строковое сравнение). Live-снапшоты (ручной ввод,
-- created_at в формате со space, другая дата) НЕ затрагиваются — SPEC-024 для
-- реального ввода сохраняется. Cashflow-график читает события напрямую — не задет.
--
-- Применение (memory d1-migrations-apply-via-execute-file): backup → wrangler d1
-- execute finances-outbox --remote --file этот_файл (НЕ migrations apply).
-- Идемпотентно: повторный прогон выставит тот же created_at.

UPDATE snapshots
   SET created_at = '2026-05-25T23:59:59Z',
       updated_at = datetime('now')
 WHERE source     = 'manual'
   AND created_at = '2026-05-25T17:23:42Z'   -- точная метка импорт-батча (198 строк, note='импорт из Finances.xlsx')
   AND deleted_at IS NULL;
